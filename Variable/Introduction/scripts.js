/*
    Var -> allow to change value
    Let -> similar to var
    Const -> constant value
*/

var n = 2
n = "pogChamp"
console.log("n = " + n) //console: n = pogChamp

let k = true
k = "pog"
console.log("k = " + k) //console: k = pog

const x = 5
x = 4 //error
console.log("x = " + x)